def _run_chat(title: str, system_prompt: str, opening_message: str):
    messages = [{"role": "assistant", "content": opening_message}]

    print("\n" + "=" * 55)
    print(f"  {title}")
    print("  （終了するには「q」と入力してください）")
    print("=" * 55)
    print(f"\nリクルーター > {opening_message}\n")

    while True:
        user_input = input("あなた > ").strip()

        if not user_input:
            continue
        if user_input.lower() in ("q", "quit", "終了"):
            print("\nお疲れさまでした！就活、応援しています！")
            break

        messages.append({"role": "user", "content": user_input})

        print("\nリクルーター > ", end="", flush=True)
        response_text = ""

        with client.messages.stream(
            model="claude-sonnet-4-6",
            max_tokens=2048,
            system=system_prompt,
            messages=messages,
        ) as stream:
            for text in stream.text_stream:
                print(text, end="", flush=True)
                response_text += text

        print("\n")
        messages.append({"role": "assistant", "content": response_text})


