# テーマ別の補足指示
_REVIEW_THEME_HINTS = {
    "ガクチカ": "目標・行動・数値結果・学びの4要素が揃っているか特に確認してください。",
    "挫折": "挫折の深さと、そこから学んだことの質を重視してください。",
    "自己PR": "強みの一貫性と、エピソードとの紐付けが自然かを重視してください。",
    "志望動機": "企業理解度と、自分のキャリアビジョンとの接続が明確かを重視してください。",
    "面接": "簡潔さ・結論ファースト・話が広がる余白があるかを重視してください。",
}


def review_text(
    text: str,
    theme: str = "ガクチカ",
    student_name: str = "",
) -> str:
    """
    ESや面接回答テキストを受け取り、Lステップ向け添削レポートを返す。

    Args:
        text         : 添削対象のESや面接回答テキスト
        theme        : テーマ（ガクチカ/挫折/自己PR/志望動機/面接）
        student_name : 学生名（ログ・参照用。レポート本文には使わない）

    Returns:
        Lステップ向けフォーマットの添削レポート文字列
    """
    hint = _REVIEW_THEME_HINTS.get(theme, "")
    system = _REVIEW_SYSTEM
    if hint:
        system += f"\n■ このテーマの追加観点\n{hint}"

    user_msg = (
        f"テーマ: {theme}\n"
        + (f"学生名: {student_name}\n" if student_name else "")
        + f"\n--- 添削対象テキスト ---\n{text}\n--- ここまで ---"
    )

    response = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=2500,
        system=system,
        messages=[{"role": "user", "content": user_msg}],
    )
    return response.content[0].text.strip()


def run_es_review():
    """
    ES/面接添削モード（CLIインタラクティブ版）。
    テキストを貼り付けると即座にLステップ向け添削レポートを出力する。
    """
    print("\n" + "=" * 55)
    print("  ES・面接添削エージェント")
    print("  （Lステップ向けフォーマットで出力します）")
    print("=" * 55)

    THEMES = {
        "1": "ガクチカ",
        "2": "挫折",
        "3": "自己PR",
        "4": "志望動機",
        "5": "面接",
    }

    print("\n📋 テーマを選んでください：")
    for k, v in THEMES.items():
        print(f"  {k}. {v}")
    print()

    while True:
        choice = input("番号を入力 > ").strip()
        if choice in THEMES:
            theme = THEMES[choice]
            break
        print("1〜5の番号を入力してください。")

    student_name = input("\n学生名（任意、Enterでスキップ） > ").strip()

    print(f"\n✏️  {theme}のESまたは回答文を貼り付けてください。")
    print("（入力が終わったら、空行のまま Enter を2回押してください）\n")

    lines = []
    empty_count = 0
    while empty_count < 2:
        line = input()
        if line == "":
            empty_count += 1
        else:
            empty_count = 0
            lines.append(line)
    text = "\n".join(lines).strip()

    if not text:
        print("テキストが入力されませんでした。")
        return

    print("\n⏳ 添削中...\n")

    try:
        report = review_text(text, theme=theme, student_name=student_name)
        print(report)
        print()

        # クリップボードへのコピー案内
        print("─" * 55)
        print("💡 上記をコピーしてLステップに貼り付けてください。")
        print("   続けて別のテキストを添削する場合は「続ける」と入力。")
        print("─" * 55)

        cont = input("\n続ける / 終了 > ").strip()
        if cont in ("続ける", "続", "y", "Y"):
            run_es_review()

    except Exception as exc:
        print(f"\n❌ エラーが発生しました: {exc}")


