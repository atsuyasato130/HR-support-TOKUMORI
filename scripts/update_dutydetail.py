def main(execute):
    sf = Salesforce(
        username=os.environ["SF_USERNAME"], password=os.environ["SF_PASSWORD"],
        security_token=os.environ["SF_SECURITY_TOKEN"], domain=os.environ.get("SF_DOMAIN", "login"),
    )
    print(f"職務内容 反映対象: {len(DUTIES)}件（{'実行' if execute else 'DRY-RUN'}）")
    if not execute:
        return
    ok = 0
    for rid, duty in DUTIES.items():
        try:
            sf.JobOfferSlip__c.update(rid, {"GfaDutyDetail__c": duty[:32000]})
            ok += 1
        except Exception as e:  # noqa: BLE001
            print(f"  ✗ {rid}: {str(e)[:90]}")
    print(f"更新完了: {ok}件")


