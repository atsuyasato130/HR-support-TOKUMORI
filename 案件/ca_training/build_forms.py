_TJ=os.path.join(BASE,"data_tests.json")
if os.path.exists(_TJ):
    TESTS=json.load(open(_TJ,encoding="utf-8"))

def norm_q(item):
    """設問を (q, type, opts, ans_list, expl) に正規化。dict / 旧tuple 両対応。"""
    if isinstance(item, dict):
        opts=list(item["opts"]); ans=item.get("ans",item.get("correct",[0]))
        if not isinstance(ans,list): ans=[ans]
        typ=item.get("type","multi" if len(ans)>1 else "single")
        return item["q"], typ, opts, ans, item.get("expl","")
    q,opts,ans=item; return q, "single", list(opts), [ans], ""

def shuffle_opts(q, opts, ans_list):
    """選択肢を決定論的に並べ替え、正解インデックス集合を再マップ。"""
    order=sorted(range(len(opts)), key=lambda j: hashlib.md5(("%s|%d"%(q,j)).encode()).hexdigest())
    opts2=[opts[j] for j in order]; ans2=sorted(order.index(a) for a in ans_list)
    return opts2, ans2

def main():
    creds=Credentials.from_authorized_user_file(TOK)
    if not creds.valid: creds.refresh(Request())
    forms=build("forms","v1",credentials=creds); sheets=build("sheets","v4",credentials=creds)
    fmap=json.load(open(MAPF)) if os.path.exists(MAPF) else {}
    if os.path.exists(_TJ):
        rec=os.environ.get("RECREATE")
        recset=set(rec.split(",")) if rec else set(TESTS.keys())
        for mid in list(recset): fmap.pop(mid,None)  # 難化テストで作り直し(RECREATE指定があればその分だけ)
    cur=sheets.spreadsheets().values().get(spreadsheetId=SSID,range="カリキュラム一覧!B6:B100").execute().get("values",[])
    rowof={r[0]:6+i for i,r in enumerate(cur) if r}

    for mid,T in TESTS.items():
        if mid in fmap:
            fid=fmap[mid]; print("skip(create) exists:",mid,fid)
        else:
            title="%s 確認テスト｜Tokumori新卒研修"%T["name"]
            f=forms.forms().create(body={"info":{"title":title,"documentTitle":title}}).execute()
            fid=f["formId"]
            nq=len(T["q"]); pv=max(1,round(100/nq)); total=pv*nq; passln=round(total*0.9)
            reqs=[
              {"updateSettings":{"settings":{"quizSettings":{"isQuiz":True}},"updateMask":"quizSettings.isQuiz"}},
              {"updateFormInfo":{"info":{"description":"%d点満点（%d問×%d点）／合格 %d点以上／単一・複数選択・自動採点。資料を読み込んでいないと満点は取れない難度です。複数選択は全て正しく選んで正解。提出後に各問の解説（正解＋復習先）が表示されます。すべて研修スライド・YouTube教材で確認できます。"%(total,nq,pv,passln)},"updateMask":"description"}},
            ]
            for i,item in enumerate(T["q"]):
                q,typ,opts,ans,expl=norm_q(item)
                opts,ans=shuffle_opts(q,opts,ans)
                qtype="CHECKBOX" if (typ=="multi" or len(ans)>1) else "RADIO"
                correct=[{"value":opts[a]} for a in ans]
                ansstr="／".join(opts[a] for a in ans)
                fb="正解：%s ｜ %s"%(ansstr, expl) if expl else ("正解：%s ｜ 復習：「%s」の資料・YouTube教材で確認できます。"%(ansstr,T["name"]))
