function setupAll() {
  var ss = SpreadsheetApp.getActive();
  ss.setSpreadsheetTimeZone('Asia/Tokyo');

  var names = [SH.TOC, SH.IMAP, SH.IDET, SH.JMAP, SH.JDET, SH.RANK, SH.TIPS, SH.DEEP];
  names.forEach(function (n) { sheet_(ss, n); });
  reorderSheets_(ss, names);

  var tocGid = ss.getSheetByName(SH.TOC).getSheetId();

  build01_imap_(ss, tocGid);
  build02_idet_(ss, tocGid);
  build03_jmap_(ss, tocGid);
  build04_jdet_(ss, tocGid);
  build05_rank_(ss, tocGid);
  build06_tips_(ss, tocGid);
  build07_deep_(ss, tocGid);
  build00_toc_(ss); // 目次は他タブのgidを参照するため最後

  deleteDefault_(ss);
  ss.getSheetByName(SH.TOC).activate();
  try { ss.toast('CA研修シートを生成しました', '完了', 5); } catch (e) {}
}

